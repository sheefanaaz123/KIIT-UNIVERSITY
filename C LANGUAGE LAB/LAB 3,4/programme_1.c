#include<stdio.h>
int main()
{
    char a;
    int b;
    float c;
    printf("Enter the character here::\n");
    scanf("%c",&a);
    printf("The character is - %c\n",a);
    printf("Enter the integer here ::\n");
    scanf("%d",&b);
    printf("The integer is - %d\n",b);
    printf("Enter the float here ::\n");
    scanf("%f",&c);
    printf("The float is - %f\n",c);
 
    return 0;
}
